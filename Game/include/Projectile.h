#ifndef PROJECTILE_H
#define PROJECTILE_H

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include <Attack.h>

////////////////////////////////////////////////////////////
/// \brief Distance specific attack
///
////////////////////////////////////////////////////////////
class Projectile : public Attack
{
    public:
    	////////////////////////////////////////////////////////////
        /// \brief Constructor
        ///
        /// \param mvtSpeed Movement speed
        ///
        /// Create an \link Attack with movement speed
        ///
        ////////////////////////////////////////////////////////////
        Projectile(int id, const sf::FloatRect box, int damage, float atqSpeed, sf::Vector2f mvtSpeed);

        ////////////////////////////////////////////////////////////
        /// \brief Destructor
        ///
        ////////////////////////////////////////////////////////////
        virtual ~Projectile();

        sf::Vector2f getMvtSpeed(){ return ms; }

        ////////////////////////////////////////////////////////////
        /// \brief Setter of the X component of projectile speed
        ///
        /// \param val New value
        ///
        ////////////////////////////////////////////////////////////
        void setMvtSpeedX(float val) { ms.x = val; }

        ////////////////////////////////////////////////////////////
        /// \brief Setter of the Y component of projectile speed
        ///
        /// \param val New value
        ///
        ////////////////////////////////////////////////////////////
        void setMvtSpeedY(float val) { ms.y = val; }

        ////////////////////////////////////////////////////////////
        /// \brief Determine the attack type
        ///
        /// Return true if the attack is a hand to hand attack
        ///
        ////////////////////////////////////////////////////////////
        bool isHtH(){ return false; }

        ////////////////////////////////////////////////////////////
        /// \brief Determine the attack type
        ///
        /// Return true if the attack is a distance attack
        ///
        ////////////////////////////////////////////////////////////
        bool isProjectile(){ return true; }

        ////////////////////////////////////////////////////////////
        /// \brief Moving projectile creation
        ///
        /// Return a moving Projectile instance
        ///
        ////////////////////////////////////////////////////////////
        Projectile* create(sf::Time t);

    protected:

    private:
    	////////////////////////////////////////////////////////////
        // Member data
        ////////////////////////////////////////////////////////////
        sf::Vector2f ms;	///Movement speed
};

#endif // PROJECTILE_H
